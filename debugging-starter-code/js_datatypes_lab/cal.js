
var num1 = prompt ("please enter first number");
var operator = prompt ("please enter your operator")
var num2 = prompt ("please enter second number");

console.log (num1);
console.log (operator);
console.log (num2);

a = parseInt(num1);
b = parseInt(num2);


if (operator == "+") {
  num1 + num2
  console.log (a+b);
}
if (operator == "-") {
  num1 - num2
  console.log (a-b);
}
if (operator == "*") {
  num1 * num2
  console.log (a*b);
}
if (operator == "/") {
  num1 / num2
  console.log (a/b);
}
